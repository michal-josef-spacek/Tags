=head1 NAME

 Tags2 - Overview about Tags2 classes.

=head1 STRUCTURE

 Perl structure:

 Reference to array.
 [type, data]
 data are:
 - normal scalar.
 - reference to scalar.

 Types:
 a  - Tag attribute.
 b  - Begin of tag.
 c  - Comment section.
 cd - Cdata section.
 d  - Data section.
 e  - End of tag.
 i  - Instruction section.
 r  - Raw section.

