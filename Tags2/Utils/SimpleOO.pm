package Tags2::Utils::SimpleOO;
#------------------------------------------------------------------------------
# $Id: SimpleOO.pm,v 1.2 2007-01-24 13:02:11 skim Exp $

# Pragmas.
use strict;

# Modules.
use Tags2;

# Version.
our $VERSION = 0.01;

# Inheritance.
our @ISA = qw(Tags2);

#------------------------------------------------------------------------------
sub new {
#------------------------------------------------------------------------------
# Constructor.

	my $class = shift;
	my $self = bless $class->SUPER::new(@_), $class;

	# Object.
	return $self;
}

#------------------------------------------------------------------------------
sub put_c {
#------------------------------------------------------------------------------
# Put comment.

	my $self = shift;
	$self->put(['c', @_]);
}

#------------------------------------------------------------------------------
sub put_b {
#------------------------------------------------------------------------------
# Put begin of tag.

	my $self = shift;
	$self->put(['b', @_]);
}

#------------------------------------------------------------------------------
sub put_e {
#------------------------------------------------------------------------------
# Put end of tag.

	my $self = shift;
	$self->put(['e', @_]);
}

#------------------------------------------------------------------------------
sub put_i {
#------------------------------------------------------------------------------
# Put instruction.

	my $self = shift;
	$self->put(['i', @_]);
}

#------------------------------------------------------------------------------
sub put_d {
#------------------------------------------------------------------------------
# Put data.

	my $self = shift;
	$self->put(['d', @_]);
}

#------------------------------------------------------------------------------
sub put_a {
#------------------------------------------------------------------------------
# Put attributes.

	my $self = shift;
	$self->put(['a', @_]);
}

1;
